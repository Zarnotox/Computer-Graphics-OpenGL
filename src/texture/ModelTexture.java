package texture;

/**
 * @author Bert
 *
 */
public class ModelTexture {
	
	/**
	 * The ID of the loaded texture
	 */
	private int textureID;

	/**
	 * @param textureID
	 */
	public ModelTexture( int textureID )
	{
		super();
		this.textureID = textureID;
	}

	/**
	 * @return the textureID
	 */
	public int getTextureID()
	{
		return this.textureID;
	}
	
	
	
	
}
